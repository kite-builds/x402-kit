export function dockerignoreContents(): string {
  return `node_modules
.git
.gitignore
*.db
*.db-journal
.env
.DS_Store
dist
.cache
`;
}

interface FileSpec {
  path: string;
  contents: string;
}

async function writeFiles(
  cwd: string,
  files: FileSpec[],
  force: boolean,
): Promise<{ written: string[]; skipped: string[] }> {
  const written: string[] = [];
  const skipped: string[] = [];
  for (const f of files) {
    const full = resolve(cwd, f.path);
    if (existsSync(full) && !force) {
      skipped.push(f.path);
      continue;
    }
    await writeFile(full, f.contents, "utf8");
    written.push(f.path);
  }
  return { written, skipped };
}

async function runFlyctl(
  flyctl: string,
  cwd: string,
  args: string[],
): Promise<number> {
  return new Promise((res, rej) => {
    const p = spawn(flyctl, args, { cwd, stdio: "inherit" });
    p.on("error", rej);
    p.on("exit", (code) => res(code ?? 1));
  });
}

export async function deploy(opts: DeployOptions = {}): Promise<DeployResult> {
  const cwd = opts.cwd ?? process.cwd();
  const provider = opts.provider ?? "fly";
  if (provider !== "fly") {
    throw new Error(`unsupported provider "${provider}" — only "fly" is supported`);
  }

  const configFile = opts.configFile ?? "x402-kit.yaml";
  const configPath = resolve(cwd, configFile);
  if (!existsSync(configPath)) {
    throw new Error(
      `config file not found at "${configPath}" — run \`x402-kit init <name>\` first or pass --config-file <path>`,
    );
  }

  const yamlSrc = await readFile(configPath, "utf8");
  let yamlDoc: unknown;
  try {
    yamlDoc = parseYaml(yamlSrc);
  } catch (err) {
    throw new Error(
      `failed to parse "${configFile}" as YAML: ${err instanceof Error ? err.message : String(err)}`,
    );
  }
  if (!yamlDoc || typeof yamlDoc !== "object") {
    throw new Error(`"${configFile}" did not parse to an object`);
  }

  const appName = opts.appName ?? deriveAppName(cwd);
  validateAppName(appName);
  const region = opts.region ?? DEFAULT_REGION;

  const files: FileSpec[] = [
    { path: "Dockerfile", contents: dockerfileContents() },
    { path: "fly.toml", contents: flyTomlContents(appName, region) },
    { path: ".dockerignore", contents: dockerignoreContents() },
  ];

  const { written, skipped } = await writeFiles(cwd, files, Boolean(opts.force));

  const nextSteps: string[] = [];
  let launched = false;

  if (opts.launch) {
    const flyctl = opts.flyctl ?? "flyctl";
    const code = await runFlyctl(flyctl, cwd, [
      "launch",
      "--copy-config",
      "--no-deploy",
      "--name",
      appName,
      "--region",
      region,
    ]);
    if (code !== 0) {
      throw new Error(
        `flyctl exited with code ${code} — check that you're authenticated (\`flyctl auth login\`) and the app name "${appName}" is available`,
      );
    }
    const deployCode = await runFlyctl(flyctl, cwd, ["deploy"]);
    if (deployCode !== 0) {
      throw new Error(`flyctl deploy exited with code ${deployCode}`);
    }
    launched = true;
    nextSteps.push(`live at https://${appName}.fly.dev/`);
    nextSteps.push(`metrics at https://${appName}.fly.dev/__x402/metrics`);
  } else {
    nextSteps.push(`flyctl auth login           # one-time, opens browser`);
    nextSteps.push(`flyctl launch --copy-config --no-deploy --name ${appName} --region ${region}`);
    nextSteps.push(`flyctl deploy`);
    nextSteps.push(`# or rerun with --launch to do the above three for you`);
  }

  return { appName, region, filesWritten: written, filesSkipped: skipped, nextSteps, launched };
}
