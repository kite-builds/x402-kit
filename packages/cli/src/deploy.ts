import { readFile, writeFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import { resolve, basename } from "node:path";
import { spawn } from "node:child_process";
import { parse as parseYaml } from "yaml";

export type DeployProvider = "fly";

export interface DeployOptions {
  cwd?: string;
  provider?: DeployProvider;
  appName?: string;
  region?: string;
  configFile?: string;
  force?: boolean;
  launch?: boolean;
  flyctl?: string;
}

export interface DeployResult {
  appName: string;
  region: string;
  filesWritten: string[];
  filesSkipped: string[];
  nextSteps: string[];
  launched: boolean;
}

const APP_NAME_RE = /^[a-z0-9][a-z0-9-]{0,29}$/;
const DEFAULT_REGION = "ams";

export function deriveAppName(projectDir: string): string {
  const base = basename(resolve(projectDir));
  const slug = base
    .toLowerCase()
    .replace(/[^a-z0-9-]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 30);
  if (!slug) {
    throw new Error(
      `cannot derive a Fly app name from directory "${base}" — pass --app-name explicitly`,
    );
  }
  return slug;
}

export function validateAppName(name: string): void {
  if (!APP_NAME_RE.test(name)) {
    throw new Error(
      `invalid Fly app name "${name}" — use 1-30 lowercase letters, digits, and hyphens (start with a letter or digit)`,
    );
  }
}

export function dockerfileContents(): string {
  return `# Generated by \`x402-kit deploy\`. Edit freely; rerun with --force to regenerate.
FROM node:22-bookworm-slim AS build
WORKDIR /app
COPY package.json package-lock.jso[n] ./
RUN npm install --omit=dev --no-audit --no-fund \\
 || (echo "no package-lock.json — falling back to npm install" && npm install --omit=dev --no-audit --no-fund)
COPY . .
RUN if [ -f tsconfig.json ]; then npx --yes tsc -p tsconfig.json; fi

FROM node:22-bookworm-slim AS runtime
WORKDIR /app
ENV NODE_ENV=production
ENV PORT=3402
COPY --from=build /app /app
RUN mkdir -p /data && chown -R node:node /app /data
USER node
VOLUME ["/data"]
EXPOSE 3402
CMD ["node", "dist/server.js"]
`;
}

